from ecommerce_order_service.mq.consumer import HANDLER_DISPATCH


class TestHandlerDispatch:
    """Tests for HANDLER_DISPATCH completeness and shape."""

    def test_all_events_in_dispatch(self) -> None:
        """Every expected event is in HANDLER_DISPATCH."""
        expected_events = [
            "OrderStatusChanged",
            "PaymentProcessed",
            "PaymentFailed",
            "PaymentRefunded",
            "ShipmentCreated",
            "ShipmentDispatched",
            "ShipmentDelivered",
            "ShipmentFailed",
        ]
        for event_name in expected_events:
            assert event_name in HANDLER_DISPATCH, (
                f"Missing HANDLER_DISPATCH entry for event: {event_name!r}"
            )

    def test_dispatch_values_are_two_tuples(self) -> None:
        """Each HANDLER_DISPATCH value is a (schema_cls, handler_fn) 2-tuple."""
        for event_name, value in HANDLER_DISPATCH.items():
            assert isinstance(value, tuple), (
                f"HANDLER_DISPATCH[{event_name!r}] must be a tuple, got {type(value)}"
            )
            assert len(value) == 2, (
                f"HANDLER_DISPATCH[{event_name!r}] must be (schema_cls, handler_fn), "
                f"got length {len(value)}"
            )

    def test_handlers_are_callable(self) -> None:
        """Each handler in HANDLER_DISPATCH is callable."""
        for event_name, (schema_cls, handler_fn) in HANDLER_DISPATCH.items():
            assert callable(handler_fn), (
                f"HANDLER_DISPATCH[{event_name!r}] handler must be callable, got {type(handler_fn)}"
            )


