def parse_test_results(test_output: str) -> dict:
    """Parse test results from pytest output."""
    results: dict = {
        "passed": 0,
        "failed": 0,
        "errors": 0,
        "skipped": 0,
        "collection_errors": 0,
        "has_collection_errors": False,
        "coverage_below_threshold": False,
        "coverage_percent": None,
    }

    # Check for collection errors
    match = re.search(r"(\d+) errors? during collection", test_output)
    if match:
        results["collection_errors"] = int(match.group(1))
        results["has_collection_errors"] = True

    if "Interrupted" in test_output and "during collection" in test_output:
        results["has_collection_errors"] = True

    # Parse passed/failed/errors/skipped from pytest summary line only.
    # Pytest summary lines look like: "===== 35 passed in 8.48s ====="
    # Require leading "=" to avoid matching unrelated output
    # (e.g. OpenTelemetry "Transient error ... retrying in 0.89s").
    summary_line = None
    for line in test_output.split("\n"):
        line = line.strip()
        if (
            line.startswith("=")
            and ("passed" in line or "failed" in line or "error" in line)
            and re.search(r" in \d", line)
        ):
            summary_line = line
    if not summary_line:
        summary_line = test_output

    match = re.search(r"(\d+) passed", summary_line)
    if match:
        results["passed"] = int(match.group(1))

    match = re.search(r"(\d+) failed", summary_line)
    if match:
        results["failed"] = int(match.group(1))

    match = re.search(r"(\d+) error", summary_line)
    if match:
        results["errors"] = int(match.group(1))

    match = re.search(r"(\d+) skipped", summary_line)
    if match:
        results["skipped"] = int(match.group(1))

    if "FAIL Required test coverage" in test_output:
        results["coverage_below_threshold"] = True

    # Extract coverage percentage
    match = re.search(r"TOTAL\s+\d+\s+\d+\s+\d+\s+\d+\s+([\d.]+)%", test_output)
    if match:
        results["coverage_percent"] = float(match.group(1))
    else:
        match = re.search(r"Total coverage:\s+([\d.]+)%", test_output)
        if match:
            results["coverage_percent"] = float(match.group(1))

    return results


def run_service_tests(
    service_dir: Path,
    args: argparse.Namespace,
    config: Optional[dict],
    env_vars: dict[str, str],
    save_output: bool,
    output_dir: Optional[Path],
) -> dict:
    """Run tests for a single service."""
    service_name = service_dir.name
    print_info("")
    print_info(f"Testing: {service_name}")
    print_info("-" * 40)

    # Install dependencies if requirements.txt exists
    requirements_path = service_dir / "requirements.txt"
    if requirements_path.exists():
        if args.details:
            print_info("  Installing dependencies...")
        subprocess.run(
            [
                sys.executable,
                "-m",
                "pip",
                "install",
                "-r",
                str(requirements_path),
                "-q",
            ],
            capture_output=True,
        )

    # Install test/dev dependencies if requirements-dev.txt exists
    dev_requirements_path = service_dir / "requirements-dev.txt"
    if dev_requirements_path.exists():
        if args.details:
            print_info("  Installing dev/test dependencies...")
        subprocess.run(
            [
                sys.executable,
                "-m",
                "pip",
                "install",
                "-r",
                str(dev_requirements_path),
                "-q",
            ],
            capture_output=True,
        )

    # Set environment variables for tests
    test_env = os.environ.copy()
    for key, value in env_vars.items():
        test_env.setdefault(key, value)
    test_env["ENVIRONMENT"] = "test"

    if getattr(args, "debug", False):
        test_env["LOG_LEVEL"] = "DEBUG"

    # Add service src to PYTHONPATH
    src_path = str(service_dir / "src")
    test_env["PYTHONPATH"] = src_path + os.pathsep + test_env.get("PYTHONPATH", "")

    # Build pytest command
    test_args: list[str] = [sys.executable, "-m", "pytest"]

    if args.unit:
        test_args.append("tests/unit")
        test_args.extend(
            [
                "--ignore-glob=tests/unit/test_models.py",
                "--ignore-glob=tests/unit/test_*_logic.py",
                "--ignore-glob=tests/unit/test_functions.py",
                "--ignore-glob=tests/unit/test_database.py",
            ]
        )
        print_info("  Running unit tests only...")
    elif args.integration:
        test_args.append("tests/integration")
        print_info("  Running integration tests only...")
    else:
        test_args.append("tests/")
        print_info("  Running all tests...")

    if args.details:
        test_args.extend(["-vv", "--tb=long", "--durations=10", "-s"])
    else:
        test_args.extend(["-v", "--tb=short"])

    if args.coverage or args.details:
        test_args.extend(
            ["--cov=app", "--cov-report=html", "--cov-report=term-missing"]
        )
        if config and config.get("test", {}).get("coverage", {}).get("threshold"):
            threshold = config["test"]["coverage"]["threshold"]
            test_args.append(f"--cov-fail-under={threshold}")

    # Marker filters from config
    if not args.unit and not args.integration:
        exclude_markers = (
            config.get("test", {}).get("markers", {}).get("exclude")
            if config
            else ["integration", "e2e"]
        )
        if exclude_markers:
            exclude_str = " or ".join(exclude_markers)
            test_args.extend(["-m", f"not ({exclude_str})"])

        include_markers = (
            config.get("test", {}).get("markers", {}).get("include") if config else None
        )
        if include_markers:
            include_str = " or ".join(include_markers)
            test_args.extend(["-m", include_str])

    # Run pytest with real-time output streaming
    process = subprocess.Popen(
        test_args,
        cwd=service_dir,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        env=test_env,
        bufsize=1,
    )

    output_lines: list[str] = []
    for line in process.stdout:
        line = line.rstrip("\n\r")
        output_lines.append(line)
        print(line, flush=True)

    returncode = process.wait()
    test_output = "\n".join(output_lines)

    # Save output to file
    if save_output and output_dir:
        test_output_file = output_dir / f"{service_name}-tests.log"
        with open(test_output_file, "w", encoding="utf-8") as f:
            f.write(test_output)

    results = parse_test_results(test_output)
    results["service_name"] = service_name
    results["test_output"] = test_output
    results["exit_code"] = returncode

    return results


def display_service_results(results: dict, args: argparse.Namespace) -> None:
    """Display test results for a service."""
    test_output = results["test_output"]

    if results["has_collection_errors"]:
        print_error(
            f"  COLLECTION ERRORS: {results['collection_errors']} collection errors"
        )
        error_lines = [
            line
            for line in test_output.split("\n")
            if re.search(r"ERROR|Error|ModuleNotFoundError|NameError|ImportError", line)
        ]
        if error_lines:
            print_error("  Collection errors:")
            for line in error_lines[:10]:
                print(f"    {line}", file=sys.stderr)
        else:
            lines = test_output.split("\n")
            for line in lines[-20:]:
                print(f"    \033[93m{line}\033[0m")

    elif results["failed"] == 0 and results["errors"] == 0 and results["passed"] > 0:
        cov_warn = (
            " [coverage below threshold]" if results["coverage_below_threshold"] else ""
        )
        passed = results["passed"]
        skipped = results["skipped"]
        print_success(f"  PASSED: {passed} tests ({skipped} skipped){cov_warn}")

        if (args.coverage or args.details) and results["coverage_percent"]:
            print_info(f"  Coverage: {results['coverage_percent']}%")

        if args.coverage or args.details:
            print_info("  Coverage report: htmlcov/index.html")

        if args.details:
            print_info("")
            print_info("  Test Summary:")
            coverage_lines = [
                line
                for line in test_output.split("\n")
                if re.match(r"^(app/|Name\s+|TOTAL\s+|---)", line)
            ]
            for line in coverage_lines[-30:]:
                print(f"    \033[90m{line}\033[0m")

            if "slowest" in test_output or "durations" in test_output:
                print_info("")
                in_durations = False
                duration_count = 0
                for line in test_output.split("\n"):
                    if "slowest" in line.lower() or "durations" in line.lower():
                        in_durations = True
                    if in_durations:
                        print(f"    \033[90m{line}\033[0m")
                        duration_count += 1
                        if duration_count > 12:
                            break

    elif results["failed"] > 0 or results["errors"] > 0:
        p, f, e = results["passed"], results["failed"], results["errors"]
        if e > 0:
            print_error(f"  FAILED: {p} ok, {f} failed, {e} errors")
        else:
            print_error(f"  FAILED: {p} passed, {f} failed")

        print_error("")
        print_error("  Failed Tests:")
        print_error("  " + "-" * 40)

        summary_started = False
        for line in test_output.split("\n"):
            if re.match(r"^=+ short test summary", line):
                summary_started = True
            if summary_started and re.search(r"(FAILED|ERROR) ", line):
                print(f"    \033[91m{line}\033[0m", file=sys.stderr)

        if args.details:
            print_error("")
            print_error("  Error Details:")
            print_error("  " + "-" * 40)
            error_lines = [
                line
                for line in test_output.split("\n")
                if re.search(r"(AssertionError|FAILED|ERROR|assert)", line)
            ]
            for line in error_lines[:20]:
                print(f"    \033[93m{line}\033[0m")

        print_error("  " + "-" * 40)
        print_info("  Tip: Run with --details for more information")
    else:
        print_warning("  NO TESTS: No tests were collected or run")


def main() -> int:
    """Run tests for all generated services."""
    args = parse_args()

    # Enable coverage automatically with --details
    if args.details:
        args.coverage = True

    # Get project root (parent of tests/ folder)
    script_dir = Path(__file__).parent
    project_path = script_dir.parent

    if not project_path.exists():
        print_error(f"Project path does not exist: {project_path}")
        return 1

    # Load test configuration (optional)
    config_path = script_dir / "test-config.json"
    config = load_test_config(config_path)

    # Setup output directory for test results
    save_output, output_dir = setup_output_directory(args, project_path)

    print_info("=" * 40)
    print_info(f"Running Tests: {project_path}")
    print_info("=" * 40)

    # Resolve service directories from SERVICES list
    services = [project_path / str(svc["dir"]) for svc in SERVICES]
    services = [s for s in services if s.is_dir() and (s / "tests").exists()]

    if not services:
        print_error("No services with tests found in project.")
        return 1

    service_names = ", ".join(s.name for s in services)
    print_info(f"Found {len(services)} services: {service_names}")

    # Load environment variables from .env if it exists
    env_path = project_path / ".env"
    env_vars = load_env_file(env_path)

    print_info("")
    print_info("Running tests...")
    print_info("=" * 40)

    total_passed = 0
    total_failed = 0
    total_errors = 0
    total_skipped = 0
    failed_services: list[str] = []
    service_coverages: list[dict[str, object]] = []
    all_results: list[dict] = []

    if args.parallel:
        print_info(f"Running tests in parallel (max {args.max_workers} workers)...")
        print_info("")

        completed_count = 0
        with ThreadPoolExecutor(
            max_workers=min(len(services), args.max_workers)
        ) as executor:
            future_to_service = {
                executor.submit(
                    run_service_tests,
                    service,
                    args,
                    config,
                    env_vars,
                    save_output,
                    output_dir,
                ): service
                for service in services
            }

            for future in as_completed(future_to_service):
                service = future_to_service[future]
                completed_count += 1

                try:
                    results = future.result()
                    all_results.append(results)

                    svc_name = results["service_name"]
                    n = len(services)
                    print_info(f"[{completed_count}/{n}] Completed: {svc_name}")
                    print_info("-" * 40)

                    total_passed += results["passed"]
                    total_failed += results["failed"]
                    total_errors += results["errors"]
                    total_skipped += results["skipped"]

                    display_service_results(results, args)

                    if results["coverage_percent"] is not None:
                        service_coverages.append(
                            {
                                "service": results["service_name"],
                                "coverage": results["coverage_percent"],
                            }
                        )

                    if (
                        results["has_collection_errors"]
                        or results["failed"] > 0
                        or results["errors"] > 0
                        or (
                            results["passed"] == 0
                            and not results["has_collection_errors"]
                        )
                    ):
                        failed_services.append(results["service_name"])

                except Exception as e:
                    n = len(services)
                    print_error(f"[{completed_count}/{n}] Error: {service.name}: {e}")
                    failed_services.append(service.name)

    else:
        for service in services:
            try:
                results = run_service_tests(
                    service, args, config, env_vars, save_output, output_dir
                )
                all_results.append(results)

                total_passed += results["passed"]
                total_failed += results["failed"]
                total_errors += results["errors"]
                total_skipped += results["skipped"]

                display_service_results(results, args)

                if results["coverage_percent"] is not None:
                    service_coverages.append(
                        {
                            "service": results["service_name"],
                            "coverage": results["coverage_percent"],
                        }
                    )

                if (
                    results["has_collection_errors"]
                    or results["failed"] > 0
                    or results["errors"] > 0
                    or (results["passed"] == 0 and not results["has_collection_errors"])
                ):
                    failed_services.append(results["service_name"])

            except Exception as e:
                print_error(f"  ERROR: {e}")
                failed_services.append(service.name)

    # Summary
    print_info("")
    print_info("=" * 40)
    print_info("Test Summary")
    print_info("=" * 40)
    print_info(f"Total Passed:  {total_passed}")
    if total_failed > 0:
        print_error(f"Total Failed:  {total_failed}")
    if total_errors > 0:
        print_error(f"Total Errors:  {total_errors}")
    if total_skipped > 0:
        print_info(f"Total Skipped: {total_skipped}")

    if (args.coverage or args.details) and service_coverages:
        print_info("")
        print_info("Coverage Summary:")
        print_info("-" * 40)
        for svc in service_coverages:
            padded = str(svc["service"]).ljust(20)
            print_info(f"  {padded} {svc['coverage']}%")

        if len(service_coverages) > 1:
            avg_coverage = sum(float(s["coverage"]) for s in service_coverages) / len(
                service_coverages
            )
            print_info(f"  Average Coverage: {avg_coverage:.2f}%")

    if failed_services:
        print_error("")
        print_error(f"Failed services: {', '.join(failed_services)}")

    total_issues = total_failed + total_errors
    if total_issues > 0 or failed_services:
        print_error("")
        print_error("Tests FAILED!")
        return 1
    elif total_passed == 0:
        print_error("")
        print_error("NO TESTS COLLECTED — nothing passed!")
        return 1
    else:
        print_success("")
        print_success("All tests PASSED!")
        return 0


if __name__ == "__main__":
    sys.exit(main())
